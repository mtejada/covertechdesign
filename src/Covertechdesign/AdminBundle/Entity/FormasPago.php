<?php

namespace Covertechdesign\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * FormasPago
 */
class FormasPago
{
    /**
     * @var string
     */
    private $nombre;

    /**
     * @var integer
     */
    private $idformaPago;


    /**
     * Set nombre
     *
     * @param string $nombre
     * @return FormasPago
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Get idformaPago
     *
     * @return integer 
     */
    public function getIdformaPago()
    {
        return $this->idformaPago;
    }
}

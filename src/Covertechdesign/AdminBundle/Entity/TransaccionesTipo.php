<?php

namespace Covertechdesign\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * TransaccionesTipo
 */
class TransaccionesTipo
{
    /**
     * @var string
     */
    private $nombre;

    /**
     * @var integer
     */
    private $idtransaccionTipo;


    /**
     * Set nombre
     *
     * @param string $nombre
     * @return TransaccionesTipo
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Get idtransaccionTipo
     *
     * @return integer 
     */
    public function getIdtransaccionTipo()
    {
        return $this->idtransaccionTipo;
    }
}
